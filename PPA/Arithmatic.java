package Marvellous;

public class Arithmatic
{
    public int iValue1;
    public int iValue2;

    public Arithmatic( int iA, int iB)
    {
        iValue1 = iA;
        iValue2 = iB;
    }

    public int Addition()
    {
        return iValue1 + iValue2;
    }

    public int Substraction()
    {
        return iValue1 - iValue2;
    }
}